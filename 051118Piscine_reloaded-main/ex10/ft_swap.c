#include<stdio.h>

void ft_swap(int *a, int *b)
{
	int c;

	c = *b;
	*b = *a;
	*a = c;
}

int	main()
{
	int	i;
	int	j;

	i = 12;
	j = 3;

	printf("i %d, j %d\n", i, j);
	ft_swap(&i, &j);
	printf("i %d, j %d\n", i, j);

	return(0);
}
